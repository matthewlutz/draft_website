// Import all logo images
const logoModules = import.meta.glob('./logos/*.png', { eager: true });

// Create a map of filename to actual imported image path
const logoImages = {};
for (const path in logoModules) {
  const filename = path.replace('./logos/', '').replace('.png', '');
  logoImages[filename] = logoModules[path].default;
}

// Get the logo URL for a college name
export function getCollegeLogo(collegeName) {
  const filename = collegeToFilename[collegeName];
  if (filename && logoImages[filename]) {
    return logoImages[filename];
  }
  return null;
}

// Export the mapping for debugging
export { collegeToFilename, logoImages };
