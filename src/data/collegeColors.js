// Helper function to get college colors with fallback
export function getCollegeColors(collegeName) {
  // Direct match
  if (collegeColors[collegeName]) {
    return collegeColors[collegeName];
  }

  // Try partial match
  const lowerName = collegeName.toLowerCase();
  for (const [key, colors] of Object.entries(collegeColors)) {
    if (key.toLowerCase().includes(lowerName) || lowerName.includes(key.toLowerCase())) {
      return colors;
    }
  }

  // Default fallback colors (gray theme)
  return { primary: '#444444', secondary: '#888888' };
}
